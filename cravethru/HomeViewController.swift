    var currentIndex = 0
    var currentLoadedCardsArray = [TinderCard]()
    var allCardsArray = [TinderCard]()
    var valueArray = ["0","1","2","3","4"]
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
